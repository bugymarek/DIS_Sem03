    private String convertTimeToString(double value) {
        return (String.format("%.3f min", value / 60.0) + " = " + (int) Math.floor((value / 60.0) % 60.0) + " min " + String.format("%.0f", (value % 60.0)) + " sec");
    }

    private String convertTimeToMinute(double value) {
        return String.format("%.3f min", value / 60.0);
    }

    public static String formatTime(double time) {
        if (time == 0) {
            return "0";
        }

        DecimalFormat df_mh = new DecimalFormat("00");
        DecimalFormat df_s = new DecimalFormat("00.00");

        double h = (int) time / 3600;
        double m = ((int) time / 60) % 60;
        double s = ((int) time) % 60 + time - (int) time;

        return ((int) h == 0 ? "" : df_mh.format(h) + ":")
                + ((int) h == 0 && (int) m == 0 ? "" : df_mh.format(m) + ":")
                + df_s.format(s);
    }

